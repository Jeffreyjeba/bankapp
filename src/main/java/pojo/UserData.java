package pojo;

public class UserData {
	private long id;
	private long activeId;
	private long time;
	
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}

	public long getActiveId() {
		return activeId;
	}
	public void setActiveId(long activeId) {
		this.activeId = activeId;
	}
	
}
